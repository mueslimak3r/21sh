#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	char	*str;

	if (!s1 || !s2)
		return (NULL);
	i = 0;
	j = 0;
	str = (char *)malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (str == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	while (s2[j] != '\0')
	{
		str[i + j] = s2[j];
		j++;
	}
	str[i + j] = '\0';
	return (str);
}

char	*ft_strjoin_array(char **strs, char *tok)
{
	char	*ret;
	int		i;

	ret = "";
	i = 0;
	while (strs[i])
	{
		ret = ft_strjoin(ret, strs[i++]);
		if (strs[i])
			ret = ft_strjoin(ret, tok);
	}
	return (ret);
}
